    $(document).ready(function () {
        $('.single-item').slick();
    });

    $('#burger').on('click', function () {
            var condition = $("#navigation-hide a").hasClass("visual");
            if (!condition) {
                $('#navigation-hide a').addClass("visual");
            } else {
                $('#navigation-hide a').removeClass("visual");
            }
        }
    );

    $('#form button').on('click', function () {
        var form = $('#form');
        var error = false;
        form.find('input, textarea').each(function () {
            if ($(this).val() == '') {
                $(this).attr("placeholder", "Fill in the field!");
                error = true;
            }
        });

        if (!error) {
            var data = form.serialize();
            $.ajax({
                type: 'POST',
                url: 'send.php',
                dataType: 'json',
                data: data,
                beforeSend: function (data) {
                    form.find('button[type="button"]').attr('disabled', 'disabled');
                },
                success: function (data) {
                    if (data['error']) {
                        alert(data['error']);
                    } else {
                        alert('we received your letter');
                        form.find('input, textarea').each(function () {
                            $(this).val("");
                        });
                    }
                },
                error: function (xhr, ajaxOptions, thrownError) {
                    alert(xhr.status);
                    alert(thrownError);
                },
                complete: function (data) { // событие после любого исхода
                    form.find('button[type="button"]').prop('disabled', false);
                }
            });
        }
    });
</script>

